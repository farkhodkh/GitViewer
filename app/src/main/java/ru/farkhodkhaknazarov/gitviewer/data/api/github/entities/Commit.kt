class Commit(
    var sha: String,
    var node_id: String,
    var url: String,
    var html_url: String,
    var comments_url: String,
    var author: Author?,
    var committer: Committer?,
    var parents: ArrayList<Parent>,
    var commit: CommitDetails?
)

class Author(
    var login: String,
    var id: Int,
    var node_id: String,
    var avatar_url: String,
    var gravatar_id: String?,
    var url: String,
    var html_url: String,
    var followers_url: String,
    var following_url: String,
    var gists_url: String,
    var starred_url: String,
    var subscriptions_url: String,
    var organizations_url: String,
    var repos_url: String,
    var events_url: String,
    var received_events_url: String,
    var type: String,
    var site_admin: Boolean
)

class Committer(
    var login: String,
    var id: Int,
    var node_id: String,
    var avatar_url: String,
    var gravatar_id: String,
    var url: String,
    var html_url: String,
    var followers_url: String,
    var following_url: String,
    var gists_url: String,
    var starred_url: String,
    var subscriptions_url: String,
    var organizations_url: String,
    var repos_url: String,
    var events_url: String,
    var received_events_url: String,
    var type: String,
    var site_admin: Boolean)

class Parent(
    var sha: String,
    var url: String,
    var html_url: String
)

class CommitDetails(
    var author: CommitAuthor,
    var committer: CommitAuthor,
    var message: String,
    var tree: Tree,
    var url: String,
    var comment_count: Int,
    var verification: Verification
)

class CommitAuthor(
    var name: String,
    var email: String,
    var date: String
)

class Tree(
    var sha: String,
    var url: String
)

class Verification(
    var verified: Boolean,
    var reason: String,
    var signature: String,
    var payload: String
)